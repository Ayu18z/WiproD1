package Day1;

public class D2Patterns {

	public static void main(String[] args) {
		
		int row = 5;
		int col = 5;
		
		for (int i = 1 ; i <=row ; i++) {
		System.out.print(i);
		
		for(int j = col; j>=0; j--) {
			System.out.println(j);
			
		}System.out.println();
	}

}
}

